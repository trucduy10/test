                  <p style="text-align:justify;"><span style="font-size:17px;"><strong>4.3 Pregnant
                        Passenger</strong></span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">Pregnant Passenger are obliged to
                      accurately inform us of the period of their pregnancy when they make a reservation and at the
                      check-in counter (pregnancy registration book or confirmation of pregnancy period of doctor). The
                      carriage of pregnant Passenger is subject to the following conditions:</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">- &nbsp; Passenger with a pregnancy of up
                      to twenty-seven (27) weeks: If the Passenger becomes pregnant normally and commits a flight within
                      the first twenty-seven (27) weeks of pregnancy, they will be accepted for carriage as a normal
                      Passenger and must meet the conditions of the authorities of the destination.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">- &nbsp; Pregnant Passenger with a
                      pregnancy of more than twenty (27) weeks to thirty-two (32) weeks (PREG): If a pregnant passenger
                      is normally pregnant, it is accepted for carriage. Passenger are responsible for presenting the
                      Certificate of state-recognized medical facility confirming that they are healthy. This document
                      is valid within seven (07) days from the date of departure. Passenger will be required to sign our
                      Disclaimer and must satisfy the conditions of the destination authority.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">- &nbsp; Pregnant from the thirty-two
                      (32) week or more or having a delivery time within seven (07) days compared to the date of
                      departure: Carriage is not accepted.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;"><strong>4.4 Oversized
                        Passenger</strong></span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">Oversized Passenger is a Passenger whose
                      body size or weight exceeds the size or load capacity of an aircraft seat.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">We do not accept carriage of oversized
                      Passenger due to the lack of service conditions.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">We reserve the right to refuse the
                      carriage of Passenger requiring the stretcher service on board.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;"><strong>4.5 Disabled
                        Passenger</strong></span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">Passenger will be assisted during the
                      boarding process. Passenger are required to notify us when making a reservation.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;"><strong>4.5.1 Blind or Visually Impaired
                        Passenger (BLND)</strong></span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">Blind or Visually impaired Passenger
                      accompanied by a companion: Accepted to be transported as a normal passenger. One Ordinary
                      Passenger can accompany a maximum of two (02) Vision-impaired Passenger.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">An unaccompanied passenger: Only accepted
                      for transportation in the following cases:</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">- &nbsp; Passenger are required to notify
                      us at least eight (08) hours in advance for domestic flights and twenty-four (24) hours in advance
                      for international flights prior to the date of departure.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">- &nbsp; Passenger have the ability to
                      travel by themselves, eat and drink on their own and are able to serve themselves during the
                      course of transportation and must satisfy the conditions of the destination authority.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">- &nbsp; Passenger must be assisted at
                      the departure and arrival airports during the journey.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">- &nbsp; We provide accompanied flight
                      attendant service. Passenger requiring a booking are required to notify us at least five (05) days
                      in advance of the departure date.</span><br><span style="font-size:17px;">Visually impaired
                      Passenger carrying a guide dog:</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">- &nbsp; Acceptance of baggage check-in,
                      not accepted to carry on board</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">- &nbsp; Free transport of guide dogs for
                      visually impaired Passenger. Each Passenger may bring a maximum of one (01) guide dog.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;"><strong>4.5.2 Deaf or Hearing-Impaired
                        Passenger (DEAF)</strong></span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">- &nbsp; Deaf or Hearing-Impaired
                      Passenger (DEAF) are understood as Passenger with hearing impairment and accompanied by an
                      accompanying passenger, and they will be accepted for carriage as a normal passenger.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">- &nbsp; Unaccompanied Passenger with
                      hearing impairment need to notify us at least eight (08) hours in advance for domestic flights and
                      twenty-four (24) hours in advance for international flights compared to the date departure, and is
                      only accepted for transportation on our flights when such Passenger is able to travel, eat and
                      take care of himself during the transportation. This confirmation information should be noted on
                      the check-in system.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;"><strong>4.5.3 Passenger with visual and
                        hearing impairment (BLND &amp; DEAF)</strong></span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">- &nbsp; Passenger with impaired vision
                      and hearing impairment accompanied by an accompanying Passenger: Accepted to be carried as normal
                      Passenger, and each of our flights is allowed a maximum of five (05) Passenger with visual and
                      hearing impairment.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">- &nbsp; Unaccompanied Passenger with
                      visual and hearing impairments: Not accepted for carriage.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;"><strong>4.5.4 Disabled Passenger with
                        intellectual or developmental disability needing assistance (DPNA)</strong></span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">Passenger must have normal expressions.
                      We do not accept Passenger with extreme signs or are likely to disturb Passenger around. We only
                      accept that Passenger with intellectual impairment must be able to travel by themselves, eat and
                      drink and serve themselves during the carriage, understand the safety instructions and must comply
                      conditions of the authority destination.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">Passenger with intellectual impairment
                      are required to travel with a relative during the flight. The relative must be assessed to be able
                      to control behavior, assist and assist guests in the process of boarding, disembarking and
                      flying.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">Information about Passenger with impaired
                      intellectual impairment and their condition must be clearly provided to us by relatives in advance
                      at least eight (08) hours prior to departure time for domestic flights, before twenty-four (24)
                      hours prior to departure for international flights.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;"><strong>4.6 Passenger using wheelchair
                        service (WCHR/S/C)</strong></span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">Passenger using wheelchair service is a
                      Passenger with limited mobility. Wheelchair services are divided into three (03)
                      categories:</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;"><strong>4.6.1 Wheelchair for Ramp
                        (WCHR)</strong></span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">As a service We provide free of charge to
                      Passenger who can move on and off the aircraft by themselves and in the passenger cabin of the
                      aircraft but need wheelchair assistance when traveling on the ground. Passenger are required to
                      notify us at least twenty-four (24) hours prior to departure time.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;"><strong>4.6.2 Wheelchair for Steps
                        (WCHS)</strong></span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">As a service We provide free of charge to
                      Passenger who can move themselves in the Passenger cabin of the aircraft but need wheelchair
                      assistance when traveling on the ground and boarding the aircraft with the help of personnel.
                      wheelchair or wheelchair special purpose forklifts. Passenger need to notify us at least
                      twenty-four (24) hours prior to departure time.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;"><strong>4.6.3 Wheelchair for Cabin
                        (WCHC)</strong></span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">We do not provide an in-flight wheelchair
                      service (WCHC) to Passenger who are unable to move themselves at any position due to lack of
                      service conditions.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;"><strong>4.7 Passenger with medical
                        requirements (MEDA)</strong></span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">Passenger with medical requirements
                      (MEDA) are those with severe illnesses, allergies, in the stage of medical care but not to a
                      serious extent, with reduced or lost self-service. Passenger, or the Passenger whose health
                      condition worsens if they on the plane or the Passenger is suffering from a critical illness ...
                      Passenger need to notify us in advance with a medical certificate of the medical facility.
                      authority to confirm Passenger is eligible to fly, and confirmed by us to be accepted for
                      transportation. Passenger will be required to sign our Disclaimer and must satisfy the conditions
                      of the Destination Authority.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">If the Passenger loses mobility or
                      becomes unconscious during the transportation, the Passenger will be assisted to take it to the
                      nearest hospital or medical facility (if possible) and pay any costs incurred.</span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;"><strong>4.8 Passenger Requiring Medical
                        Stretcher (STCR) &amp; Oxygen Service Onboard (OXYG)</strong></span></p>
                  <p style="text-align:justify;"><span style="font-size:17px;">We do not accept the carriage of
                      Passenger using stretcher service and/or Passenger using medical oxygen service due to inability
                      to serve, except in force majeure or required by Authorities.</span></p>
